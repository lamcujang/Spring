package org.common.dbiz.exception.wrapper;

public class VendorObjectNotFoundException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public VendorObjectNotFoundException() {
		super();
	}

	public VendorObjectNotFoundException(String message, Throwable cause) {
		super(message, cause);
	}

	public VendorObjectNotFoundException(String message) {
		super(message);
	}

	public VendorObjectNotFoundException(Throwable cause) {
		super(cause);
	}
	
	
	
}










